CREATE TABLE BMIREPORT /* STORES THE BMI REPORT */
(
     USERNAME VARCHAR(40) NOT NULL,
     AGE INT,
     HEIGHT FLOAT,
     WEIGHT FLOAT,
     BMI FLOAT,
     RESULTS VARCHAR(40)
);


CREATE TABLE LOGIN /* STORES THE LOGIN DETAILS */
(
     NAME VARCHAR(40),
     USERNAME VARCHAR(40) PRIMARY KEY NOT NULL,
     PASSWORD VARCHAR(40),
     PIN VARCHAR(5)
);

CREATE TABLE GENERAL /* STORES THE GENERAL REPORT */
		 (USERNAME VARCHAR(40),
		 SYSTOLIC FLOAT,
		 DIASTOLIC FLOAT,
		FASTING FLOAT,
		POSTMEAL FLOAT,
		 RESULTBP VARCHAR(60),
		RESULTSUGAR VARCHAR(60),
		 DATE VARCHAR(40));