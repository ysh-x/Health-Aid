     public ReportFinal(String User) {
         initComponents();
         UserLabel.setText(User);
         setLocationRelativeTo(null);
         
         try {
             
             String U = UserLabel.getText();
              String url = "jdbc:h2:~/test";
           Connection C = DriverManager.getConnection(url,"sa","sa");
             String query = "Select * from GENERAL where username = '" +U + "';";
             System.out.println(query);
               Statement S = C.createStatement(); 
                ResultSet RS = S.executeQuery(query);
                
                while(RS.next()) 
                    {
                        String BP = RS.getString("RESULTBP");
                        String Sugar = RS.getString("RESULTSUGAR");
                        
                        System.out.println(BP +"\n\n\n" + Sugar);
                        BPChange(BP);
                        SugarChange(Sugar);
                    }
                    
                RS.close();
                S.close();
         
         } catch (SQLException E) { System.out.println(E); }
         
     }
