     public void BPChange(String BP) {
        
       
        
        if(BP.equals("Low MAP")) {
            BPResult.setText(BPResults[0]);
            BPResult.setBackground(new Color(255,103,27));
 
        }
        else if(BP.equals("Normal MAP")) {

            BPResult.setText(BPResults[1]); 
             BPResult.setBackground(new Color(0,103,39));
            
        }
        else if(BP.equals("High MAP")) { 

            BPResult.setText(BPResults[2]);
            BPResult.setBackground(new Color(255,57,4));
            
        }
        else if(BP.equals("null")) {
            BPResult.setText("\n   Wrong Details");
            BPResult.setBackground(new Color(255,57,40));
            
        }
 
        
    }
